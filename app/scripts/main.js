function Image(strURL, strCaption) {
	switch(true) {
		case strURL.length <= 0:
			throw new Error("The URL may not be left blank!");
			break;

		case strCaption.length <= 0:
			throw new Error("The caption may not be left blank!");
			break;

		default:
			this.url = strURL;
			this.caption = strCaption;
			break;
	}
}

